    private void btnvalidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnvalidateActionPerformed
      String emailid=txtemail.getText();
      if(emailid.equalsIgnoreCase(email)==false)
      {
          lblnotice.setText("Email id doesn't match");
          txtemail.requestFocus();
          return;
      }
      String question=(String)cmbquestion.getSelectedItem();
      String newAnsawer=txtanswer.getText();
      if(securityQuestion.equalsIgnoreCase(question))
      {
          if(answer.equalsIgnoreCase(newAnsawer))
          {
              scallBackArt();
              lblnotice.setText("Password= "+oldPassword);
              cmbquestion.setSelectedIndex(0);
              lblthanks.setText("Thank You.");
              txtanswer.setText("");
              txtemail.setText("");
              txtid.setText("");
              btnvalidate.setVisible(false);
          }
          else
              lblnotice.setText("Question and Answer Doesn't matched");
      }
      else
          lblnotice.setText("Question and Answer Doesn't matched");
          
    }//GEN-LAST:event_btnvalidateActionPerformed

