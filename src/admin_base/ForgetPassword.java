    private void getDataById()
    {
        String Id=txtid.getText();
        int counter=0;
        Connection con;
        try {
            con = ConnectionProvider.getCon();
            if(con==null)
            {
                return;
            }
            Statement st = con.createStatement();
            ResultSet rs;
            rs = st.executeQuery("select * from admin_details where admin_id="+Id);
            while (rs.next()) {
               counter++; 
               email=rs.getString(6);
               oldPassword=rs.getString(7);
               securityQuestion=rs.getString(13);
               answer=rs.getString(14);
            }
            if(counter==0)
            {
                Id=null;
                lblnotice.setText("Invalid ID");
                btnvalidate.setVisible(false);
            }
            else
            {
                counter=0;
                lblnotice.setText("You are good to go");
                btnvalidate.setVisible(true);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Invalid ID");
        }
    }
