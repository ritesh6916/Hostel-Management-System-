    private void btnsignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsignupActionPerformed
        if(img_adhar==null || img_photo==null || img_sign==null)
        {
            lblnotice1.setText("Please Select Photo, Adhar and Signature");
            return;
        }
        if(password.equals(txtconformpassword.getText())==false)
        {
            
            lble3.setText("Password Does'n Match");
            lblnotice1.setText("");
            lble3.setVisible(true);
            return;
        }
        Connection con; 
        try{
            con=ConnectionProvider.getCon();
            if(con==null)
            {
            return;
            }
            
            PreparedStatement pst=con.prepareStatement("insert into admin_details values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            pst.setString(1, id);
            pst.setString(2,txtfname.getText());
            pst.setString(3,txtlname.getText());
            pst.setString(4,gender);
            pst.setString(5,blood);
            pst.setString(6,email);
            pst.setString(7,password);
            pst.setString(8,mobile);
            pst.setString(9,adhar);
            FileInputStream adharStream=new FileInputStream(img_adhar);
            pst.setBinaryStream(10, adharStream);
            FileInputStream PhotoStream=new FileInputStream(img_photo);
            pst.setBinaryStream(11, PhotoStream);
            FileInputStream signStream=new FileInputStream(img_sign);
            pst.setBinaryStream(12, signStream);
            pst.setString(13, securityQuestion);
            pst.setString(14, answer);
            pst.setString(15, "Not Allowed");
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Admin Details Saved Successfully");
            this.setVisible(false);
            new Login().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnsignupActionPerformed

