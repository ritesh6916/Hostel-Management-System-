    private void btnvalodateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnvalodateActionPerformed
        String tempid;
        int i=0;
        tempid=txtid.getText();
        if(tempid.length()<4)
        {
            lblnotice1.setText("Invalid ID");
            txtid.requestFocus();
            return;
        }
        Connection con=ConnectionProvider.getCon();
        if(con==null)
        {
            return;
        }
        Statement st;
        try {
            st = con.createStatement();
            ResultSet rs=st.executeQuery("select * from admin_details where admin_id="+tempid);
            while(rs.next())
            {
                i++;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        if(i>=1)
        {
            lblnotice1.setText("Sorry! this ID is already Taken.");
            lblnotice1.setVisible(true);
            txtid.requestFocus();
        }
        else
        {
            lblnotice1.setText("You are Good To go");
            lblnotice1.setVisible(true);
            id=txtid.getText();
            txtfname.requestFocus();
        }
    }//GEN-LAST:event_btnvalodateActionPerformed

